export function getTranslation(language, key, fallback = key) {
  if (!key && key !== 0) return fallback || '';
  const textKey = String(key);
  return translations[language]?.text?.[textKey] || translations[language]?.ui?.[textKey] || fallback || textKey;
}

export function translateText(language, value) {
  if (value === null || value === undefined) return value;
  if (typeof value !== 'string') return value;
  return getTranslation(language, value, value);
}
